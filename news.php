      Fakultas Ilmu Komputer Universitas Brawijaya atau disingkat FILKOM merupakan
      salah satu fakultas di Universitas Brawijaya yang baru saja diresmikan pada
      Januari 2015. Sebelumnya bernama Program Teknologi Informasi dan Ilmu Komputer
      atau disingkat PTIIK yang berada sejak tahun 2011. Saat ini terdapat 3 program
      studi yang aktif di fakultas ini. Dalam tujuannya untuk mencetak lulusan yang
      kompeten dalam bidang Teknologi Informasi, FILKOM menerapkan kurikulum berbasis
      kompetensi yang merujuk pada ABET (Accreditation Board for Engineering and
      Technology). FILKOM bertujuan untuk menghasilkan lulusan yang berjiwa enterpreneur
      berbasis pada moral dan etika serta dapat dipercaya sehingga mampu bekerjasama
      dan memberikan kontribusi di tingkat nasional dan internasional.
       <br>
      Merujuk pada SK Dikti No.163/KEP/DIKTI/2007 mengenai penataan dan kodifikasi Program Studi, dibentuklah Program Teknologi Informasi dan Ilmu Komputer (PTIIK) berdasarkan Surat Keputusan Rektor Universitas Brawijaya Nomor: 516/SK/2011 tanggal 27 Oktober 2011 yang merupakan gabungan dari 2 program studi (Teknik Perangkat Lunak dari Fakultas Teknik dan Ilmu Komputer dari Fakultas MIPA) yang telah ada di Universitas Brawijaya dimana kedua program studi tersebut memiliki kesamaan dan kesesuaian hakikat sebuah disiplin ilmu. Saat ini PTIIK telah memiliki 3 program studi dan bertempat di Gedung Politeknik Lama UB, diantaranya:[2]
      Program Studi (S-1) Informatika / Ilmu Komputer
      Program Studi (S-1) Sistem Komputer
      Program Studi (S-1) Sistem Informasi <br>
      Bertujuan untuk memenuhi kebutuhan akan SDM yang profesional di bidang teknologi informasi, diharapkan PTIIK mampu untuk menjembatani antara kepentingan industri dan masyarakat profesi dengan kepentingan akademik, maka disusunlah kurikulum berbasis kompetensi, dimana selain muatan-muatan inti, diberikan pula muatan-muatan lokal yang mendukung basis pengetahuan terapan dan perekayasaan perangkat lunak. Diharapkan melalui program ini dapat dihasilkan lulusan yang memiliki daya saing, jiwa kewirausahaan, dan memiliki wawasan teknologi informasi yang memadai sehingga tidak gagap ketika tiba waktunya untuk menerapkan ilmunya di masyarakat.[2]
      Kemudian sekarang Prodi tersebut telah berkembang pesat ketika diresmikan menjadi Fakultas Ilmu Komputer Universitas Brawijaya yaitu diantaranya:
      Program Studi (S-2) Magister Ilmu Komputer
      Program Studi (S-1) Informatika / Ilmu Komputer
      Program Studi (S-1) Informatika minat Sistem Komputer
      Program Studi (S-1) Sistem Informasi
      Program Studi (S-1) Pendidikan Teknologi Informasi

